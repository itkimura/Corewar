#include "asm.h"
#include <fcntl.h>

int	read_input(t_asmdata *data, char *argv)
{
	char	*line;
	int		ret;
	int		fd;

	line = NULL;
	ret = 1;
	fd = open(argv, O_RDONLY);
	if (fd == -1)
		free_exit(data, "Open file failed", ERROR);
	if (!data)
		return (0);
	while (ret)
	{
		ret = get_next_line(fd, &line);
		parse(data, line, fd);
		ft_strdel(&line);
	}
	if (close(fd) == -1)
		free_exit(data, "Closing file failed", ERROR);
	return (1);
}
