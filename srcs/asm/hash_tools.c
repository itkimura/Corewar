#include "asm.h"

/* hash * 33 + c */
uint32_t	hash(char *str)
{
	uint32_t	hash;
	uint32_t	i;

	hash = 5381;
	i = 0;
	while (str[i])
		hash = ((hash << 5) + hash) + str[i++];
	return (hash % HASHTABLESIZE);
}
