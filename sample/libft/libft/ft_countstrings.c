#include "libft.h"

int	ft_countstrings(const char *s, char c)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			x++;
		i++;
	}
	if (x == 0)
		x = 1;
	return (x);
}
